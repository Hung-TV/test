export const FAVORITE_LIST_KEYS = ['FAVORITES', 'favorites'];
export const FAVORITE_LIST_NAMES = ['Từ yêu thích', 'Favorites'];

export const isFavoriteDictionaryList = (list) => (
  Boolean(list?.isFavorite)
  || FAVORITE_LIST_KEYS.includes(list?.systemKey)
  || FAVORITE_LIST_NAMES.includes(list?.name)
);

export const getLocalizedDictionaryGloss = (meaning, language = 'vi') => {
  if (!meaning) return '';
  if (typeof meaning === 'string') return meaning;

  const suffix = language === 'en' ? 'En' : 'Vi';
  return meaning[`gloss${suffix}`]
    || meaning[`gloss_${language}`]
    || meaning.glosses?.[language]
    || (typeof meaning.gloss === 'object' ? meaning.gloss?.[language] : null)
    || (typeof meaning.gloss === 'string' ? meaning.gloss : '');
};

export const getLocalizedExampleSentence = (example, language = 'vi') => {
  if (!example) return '';
  const suffix = language === 'en' ? 'EN' : 'VI';
  return example[`sentence${suffix}`]
    || example[`sentence_${language}`]
    || example.sentences?.[language]
    || (language === 'en' ? example.sentenceVI : example.sentenceEN)
    || '';
};

export const formatDictionaryDate = (value, language = 'vi') => {
  if (!value) return '';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '';

  return new Intl.DateTimeFormat(language === 'en' ? 'en-US' : 'vi-VN', {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
    hour12: false,
  }).format(date).replace(',', '');
};
