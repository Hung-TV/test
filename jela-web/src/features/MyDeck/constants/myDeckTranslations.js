export const getLocalizedDeckTitle = (deck, language = 'vi') => {
  if (!deck) return '';

  const suffix = language === 'en' ? 'En' : 'Vi';
  return deck[`title${suffix}`]
    || deck[`title_${language}`]
    || deck.titles?.[language]
    || (typeof deck.title === 'object' ? deck.title?.[language] : null)
    || (typeof deck.title === 'string' ? deck.title : '');
};
