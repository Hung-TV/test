  /// Lấy bản dịch cho màn hình chỉ định và khóa chỉ định dựa vào BuildContext
  static String get(BuildContext context, String section, String key) {
    final lang = Provider.of<PreferencesProvider>(context, listen: false).locale.languageCode;
    final dict = _translations[lang] ?? _translations['vi']!;
    final secDict = dict[section] ?? {};
    return secDict[key] ?? key;
  }

  /// Lấy toàn bộ map bản dịch cho một màn hình chỉ định
  static Map<String, String> getSection(BuildContext context, String section) {
    final lang = Provider.of<PreferencesProvider>(context, listen: false).locale.languageCode;
    final dict = _translations[lang] ?? _translations['vi']!;
    return dict[section] ?? {};
  }
